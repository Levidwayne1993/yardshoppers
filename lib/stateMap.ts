// Reverse map: abbreviation → full name (for display)
export const STATE_ABBR_TO_NAME: Record<string, string> = Object.fromEntries(
  Object.entries(STATE_NAME_TO_ABBR).map(([name, abbr]) => [abbr, name.replace(/\b\w/g, (c) => c.toUpperCase())])
);

/**
 * Given a search string, checks if it matches a full state name
 * and returns the abbreviation. Returns null if no match.
 */
export function resolveStateAbbreviation(query: string): string | null {
  const normalized = query.trim().toLowerCase();
  return STATE_NAME_TO_ABBR[normalized] || null;
}
